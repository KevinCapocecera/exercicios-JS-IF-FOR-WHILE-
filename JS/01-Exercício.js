// pedir um numero ao usuario
var numero = parseInt(prompt("Digite um numero"))

// verificar se o numero é positivo
if (numero > 0){
    // exibir uma mensagem informando que o numero é positivo
    document.write("O numero " + numero + " é positivo")

// verificar se o numero é negativo
}else if(numero < 0){
    // exibir uma mensagem informando que o numero é negativo
    document.write("O numero " + numero + " é negativo")

// verificar se o numero é zero
}else if(numero == 0){
    // exibir uma mensagem informando que o numero é zero
    document.write("O numero " + numero + " é zero, burro")
}
        
